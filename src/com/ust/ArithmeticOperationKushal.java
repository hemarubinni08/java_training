package com.ust;

public class ArithmeticOperationKushal {
    float result;

    public float add(float n1, float n2) {
        result = n1 + n2;
        return result;
    }

    public float subtract(float n1, float n2) {
        result = n1 - n2;
        return result;
    }

    public float multiply(float n1, float n2) {
        result = n1 * n2;
        return result;
    }

    public float division(float n1, float n2) {
        result = n1 / n2;
        return result;
    }

}
