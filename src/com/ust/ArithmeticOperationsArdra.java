package com.ust;

public class ArithmeticOperationsArdra {
    int addition(int a, int b) {
        return a + b;
    }

    int subtraction(int a, int b) {
        return a - b;
    }

    int multiply(int a, int b) {
        return a * b;
    }

    float divide(int a, int b) {
        return (float) a / (float) b;
    }
}
