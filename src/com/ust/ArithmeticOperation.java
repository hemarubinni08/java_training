package com.ust;

public class ArithmeticOperation {

    public static Integer addition(int a, int b) {
        return a + b;
    }

    public static Integer subraction(int a, int b) {
        return a - b;
    }

    public static Integer multiplication(int a, int b) {
        return a * b;
    }
    // overloading
    public static Integer addition(int a,int b ,int c){
        return a+b+c;
    }
}
